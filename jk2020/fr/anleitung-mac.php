<h2>1. Télécharger le logiciel Zoom</h2>

<p>Téléchargez le logiciel Zoom en cliquant sur le lien suivant:  <a class="btn btn-primary" href="https://zoom.us/client/latest/Zoom.pkg"><i class="fas fa-download"></i> télécharger Zoom</a></p>

<p>Lorsque l'on vous demande si vous souhaitez autoriser le téléchargement, cliquez sur <b>Autoriser.</b></p>
<img class="explainimg" src="Mac-1.png">

<p>Le programme d’installation se trouve désormais dans votre dossier Téléchargements.</p>

<h2>2. Installer</h2>


<p>Dans le navigateur Safari, cliquez sur la flèche de téléchargement en haut à droite, puis <b>double-cliquez</b> sur Zoom.pkg:</p>
<img class="explainimg" src="Mac-2.png">

<p>Vous pouvez également ouvrir le dossier <b>Downloads</b> dans le Finder et <b>double-cliquer </b> sur Zoom.pkg.</p>
<img class="explainimg" src="Mac-2-1.png">


<p>La fenêtre d’installation s’ouvre. Cliquez sur <b>Continuer</b></p>
<img class="explainimg" src="Mac-3.png">

<p>Cliquez sur <b>Installer.</b></p>
<img class="explainimg" src="Mac-4.png">

<p>Entrez votre mot de passe pour confirmer l'installation. Cliquez sur <b>Installer le logiciel</b></p>
<img class="explainimg" src="Mac-5.png">

<p>Cliquez sur <b>Ajouter à la corbeille</b></p>
<img class="explainimg" src="Mac-6.png">

<p>Si cette fenêtre s’affiche alors l'installation est terminée: </p>
<img class="explainimg" src="Mac-8.png">

<p>Vous êtes maintenant prêt à participer à une réunion de test. Continuez à <a class="btn btn-primary" href="testsession.php"><i class="fas fa-arrow-right"></i> l’étape 3</a>.</p>


