<h4>Guide rapide</h4>

<p>Pour participer, cliquez sur le bouton correspondant (ci-dessus).</p>

<p>Le programme zoom s'ouvre alors et vous demande si vous voulez rejoindre la session via l'audio par ordinateur :</p>
<img class="explainimg" src="zoom-session.png">

<p>Cliquez ensuite sur <b>Tester le haut-parleur et le micro</b>. On vous demande si vous entendez une sonnerie. Utilisez le menu déroulant pour paramétrer les possibilités jusqu’à entendre un son:</p>
<img class="explainimg" src="zoom-session-2.png">

<p>Lorsque vous entendez la sonnerie, cliquez sur <b>Oui</b></p>

<p>Probieren Sie über die Auswahl alle Möglichkeiten aus, bis Sie den Balken ausschlagen sehen. Dies dient dazu, Ihr Mikrofon zu testen.</p>
<img class="explainimg" src="zoom-session-3.png">
<p>Si vous voyez les barres bouger, cliquez sur <b>Oui</b></p>
<p>Cliquez sur <b>Participer avec l’audio par ordinateur</b></p>
<img class="explainimg" src="zoom-session-4.png">

<p>Maintenant la fenêtre suivante s’affiche :</p>
<img class="explainimg"  style="width: 100%" src="Zoom-Fenster-Markiert.png">

<p><b>Numéro 1</b></p>
<p>Dans ce cadre, vous voyez l'image de la personne qui parle en ce moment. Dans la majorité des cas, ce sera celle qui animera et dirigera des différents blocs de la conférence.</p>

<p><b>Numéro 2</b></p>
<p>Activer / Désactiver le micro : ce bouton vous permet de couper ou réactiver le son de votre micro. Lorsque l’icône symbolisant le micro est barrée, vous ne pouvez pas être entendu.</p>
<p>Lors de la conférence, il y aura des sessions en plénière et en petits groupes :</p>
<p><b>Sessions plénières</b></p>
<p>Jusqu’à 250 personnes participeront en session plénière. Nous allons donc couper et bloquer le micro lors de ces sessions (vous ne pourrez pas l'activer vous-même). Si, dans ce cas, vous souhaitez prendre la parole, vous pouvez lever la main (voir numéro 4) et ensuite l’animateur vous autorisera à vous exprimer. </p>
<p><b>Sessions en petits groupes</b></p>
<p>Dans les petits groupes vous pouvez vous-même commander votre micro. En règle générale, il convient de désactiver le micro lorsque vous ne parlez pas.</p>

<p><b>Numéro 3</b></p>
<p>Activer / Désactiver la vidéo : ce bouton vous permet de contrôler la transmission vidéo de votre propre caméra, donc de savoir si vous êtes visible ou non. Nous vous recommandons d'allumer la caméra lorsque vous parlez à d'autres personnes ou lorsque vous prenez la parole en séance plénière.</p>

<p><b>Numéro 4</b></p>
<p>Liste des participants : cliquez sur ce bouton pour voir tous les autres participants. Tout en bas à droite, il y a le bouton <b>Autoriser le public à lever la main* (Mac : Lever la main)</b>, sur lequel vous pouvez cliquer si vous souhaitez prendre la parole. L’animateur vous autorisera ensuite à parler. Une main apparaîtra alors à côté de votre nom. Veuillez patienter un instant jusqu'à ce qu’on vous donne la parole. L’animateur aura besoin d'un certain temps pour coordonner toutes les demandes de prise de parole.</p>
<p>* Le nom du bouton est malheureusement incorrect et devrait en réalité s’appeler « Lever la main »</p>
<img class="explainimg" src="hand-heben.png">


<p><b>Numéro 5</b></p>
<p>Converser : ce bouton vous permet d’envoyer de courts messages à l’animateur. Par exemple, si vous avez une question, vous pouvez la poser par ce biais. Afin de ne pas déranger tous les participants, veuillez vous assurer de bien sélectionner au préalable la personne où, à côté, (hôte) figure entre parenthèses :</p>
<img class="explainimg" src="Chatten.png">

<p><b>Numéro 6</b></p>
<p>Quitter : ce bouton vous permet de quitter la session en cours. Pendant la conférence, nous vous recommandons de rester en ligne toute la journée (et de désactiver la caméra et le micro si nécessaire). Si vous avez quitté la session par erreur, vous pouvez utiliser le bouton <b>Rejoindre</b> de cette page pour vous reconnecter.</p>
