<h4>Kurzanleitung</h4>



<p>Um teilzunehmen, klicken Sie auf den entsprechenden Knopf (oben).</p>

<p>Nun öffnet sich das Zoom-Programm und fragt Sie, ob Sie per Computer-Audio beitreten möchten:</p>
<img class="explainimg" src="zoom-session.png">

<p>Klicken Sie auf <b>Computer-Audio testen</b>. Nun werden Sie gefragt ob Sie einen Signalton hören. Probieren Sie über die Auswahl alle Möglichkeiten aus, bis Sie den Signalton hören können:</p>
<img class="explainimg" src="zoom-session-2.png">

<p>Sobald Sie den Ton hören konnten, klicken Sie auf <b>Ja</b></p>

<p>Probieren Sie über die Auswahl alle Möglichkeiten aus, bis Sie den Balken ausschlagen sehen. Dies dient dazu, Ihr Mikrofon zu testen.</p>
<img class="explainimg" src="zoom-session-3.png">
<p>Wenn Sie den Balken ausschlagen gesehen haben, klicken Sie auf <b>Ja</b></p>
<p>Klicken Sie auf <b>Per Computer dem Audio beitreten</b></p>
<img class="explainimg" src="zoom-session-4.png">

<p>Nun sehen Sie diese Anzeige:</p>
<img class="explainimg"  style="width: 100%" src="Zoom-Fenster-Markiert.png">

<p><b>Nummer 1</b></p>
<p>In diesem Bereich werden Sie das Bild der gerade sprechenden Person sehen. Mehrheitlich wird dies die Moderation und Leitung der einzelnen Blöcke an der Konferenz sein.</p>

<p><b>Nummer 2</b></p>
<p>Mikrofon stumm/laut schalten: Diesen Knopf verwenden Sie, um Ihr eigenes Mikrofon stumm oder laut zu schalten. Wenn das Mikrofonsymbol durchgestrichen ist, kann man Sie nicht hören.</p>
<p>An der Konferenz wird es Veranstaltungen im Plenum und in Kleingruppen geben:</p>
<p><b>Plenum</b></p>
<p>Im Plenum werden bis zu 250 Personen beteiligt sein. Daher werden wir in diesen Pleni das Mikrofon bei Ihnen stumm schalten und sperren (Sie können es nicht selbst aktivieren). Wenn Sie in diesem Fall sprechen möchten, können Sie die Hand heben (Siehe Nummer 4) und werden dann von der Moderation zugelassen.</p>
<p><b>Kleingruppen</b></p>
<p>In den Kleingruppen können Sie Ihr Mikrofon selbst steuern. In der Regel ist es sinnvoll, das Mikrofon zu deaktivieren, wenn Sie nicht sprechen.</p>

<p><b>Nummer 3</b></p>
<p>Video aktivieren/deaktivieren: Mit diesem Knopf können Sie die Videoübertragung Ihrer eigenen Kamera steuern, ob Sie also zu sehen sind oder nicht. Wir empfehlen Ihnen, die Kamera einzuschalten, wenn Sie mit anderen Personen sprechen oder sich im Plenum zu Wort melden.</p>

<p><b>Nummer 4</b></p>
<p>Teilnehmeransicht: Wenn Sie hier klicken sehen Sie alle anderen Teilnehmer/innen. Ganz unten rechts sehen Sie hier einen Knopf <b>Den Zuschauern gestatten, die Hand zu heben* (Mac: Hand heben).</b>Diesen klicken Sie an, wenn Sie eine Wortmeldung machen möchten. Die Moderation lässt Sie dann zu. Neben Ihrem Namen erscheint eine Hand. Bitte haben Sie jeweils einen Moment Geduld, bis Sie zugelassen werden. Die Moderation wird etwas Zeit benötigen, um alle Wortmeldungen zu koordinieren.</p>
<p>* Die Benennung des Knopfs ist leider fehlerhaft und sollte eigentlich «Hand heben» heissen</p>
<img class="explainimg" src="hand-heben.png">


<p><b>Nummer 5</b></p>
<p>Chat: Im Chat können Sie Kurznachrichten an die Moderation schicken. Wenn Sie z.B. eine Frage haben, können Sie diese dort stellen. Damit Sie nicht alle Teilnehmer stören, bitten wir Sie, vorher unbedingt die Person auszuwählen, bei der (host) in Klammern steht:</p>
<img class="explainimg" src="Chatten.png">

<p><b>Nummer 6</b></p>
<p>Verlassen: Über diesen Knopf verlassen Sie die Sitzung. Wir empfehlen Ihnen, während der Konferenz den ganzen Tag eingewählt zu bleiben (und bei Bedarf Kamera und Mikrofon zu deaktivieren). Sollten Sie die Sitzung aus Versehen verlassen haben, können Sie sich über den Knopf <b>Teilnehmen</b> auf dieser Seite wieder einwählen.</p>
